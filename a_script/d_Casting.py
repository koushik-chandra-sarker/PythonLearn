a = int(12)
print("a: ", a)  # Output: a: 12

a = int(23.33)
print(a)  # Output: a: 23

a = int("5")
print("a: ", a)  # Output: a: 5
print("type of a: ", type(a))  # Output: 3<class 'int'>

b = float(12)
print("b: ", b)  # Output: 12.0

b = float(23.33)
print("b: ", b)  # Output: 23.33

b = float("5")
print("b: ", b)  # Output: 5.0
print("type of b: ", type(b))  # Output: 3<class 'float'>
